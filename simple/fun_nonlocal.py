x = 6
def func_outer():
    x = 2  # тут х не локально, так як всередині функції
    print('x дорівнює', x)

    def func_inner():
        nonlocal x
        x = 5 # тут ще одна ступіль локального х, але так як у нас нонлокал, то впливає на вищі рівні (крім глобал) для Х

    func_inner()
    print('Локальне 1-го рівня помінялось на', x)

func_outer()
print ('Глобальне дорівнює', x)